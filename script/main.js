function navbar_open(){
    let close = document.getElementById("close")
    close.style.transform = "translateY(0px)";
    let open = document.getElementById("open")
    open.style.display="none"
    console.log("Marche")
}
function navbar_close(){
    let close = document.getElementById("close")
    close.style.transform = "translateY(0px)";
    let open = document.getElementById("open")
    open.style.display="none"
    console.log("Marche")
}